#!/usr/bin/env bash
# Hefesto one-liner installer — draft (F1).
#   curl -fsSL https://get.hefesto.sh | bash
# Installs Docker if missing, writes /opt/hefesto/{docker-compose.yml,.env}
# and starts the stack (hefesto + postgres + redis + traefik).

set -euo pipefail

HEFESTO_DIR="${HEFESTO_DIR:-/opt/hefesto}"
COMPOSE_URL="${COMPOSE_URL:-https://raw.githubusercontent.com/hefesto-sh/hefesto/main/docker/docker-compose.prod.yml}"

log() { printf '\033[1;36m[hefesto]\033[0m %s\n' "$*"; }
fail() { printf '\033[1;31m[hefesto]\033[0m %s\n' "$*" >&2; exit 1; }

[ "$(id -u)" -eq 0 ] || fail "Run as root (or via sudo)."
[ "$(uname -s)" = "Linux" ] || fail "The installer targets Linux servers."

# 1. Docker
if ! command -v docker >/dev/null 2>&1; then
  log "Installing Docker..."
  curl -fsSL https://get.docker.com | sh
fi
docker compose version >/dev/null 2>&1 || fail "Docker Compose v2 plugin is required."

# 2. Layout + compose file
mkdir -p "$HEFESTO_DIR"
log "Downloading compose file to $HEFESTO_DIR/docker-compose.yml"
curl -fsSL "$COMPOSE_URL" -o "$HEFESTO_DIR/docker-compose.yml"

# 3. .env (generated once; kept on re-run)
if [ ! -f "$HEFESTO_DIR/.env" ]; then
  log "Generating $HEFESTO_DIR/.env"
  DEFAULT_HOST="$(hostname -I 2>/dev/null | awk '{print $1}' || echo localhost)"
  # ACME_EMAIL is required for Let's Encrypt; prompt when not provided via env.
  if [ -z "${ACME_EMAIL:-}" ]; then
    if [ -t 0 ]; then
      read -rp "Email for Let's Encrypt (SSL) notifications: " ACME_EMAIL
    fi
    [ -n "${ACME_EMAIL:-}" ] || fail "Set ACME_EMAIL=you@example.com and re-run (required for SSL certificates)."
  fi
  cat > "$HEFESTO_DIR/.env" <<EOF
# Hefesto configuration — generated by install.sh
HEFESTO_HOST=${HEFESTO_HOST:-$DEFAULT_HOST}
ACME_EMAIL=$ACME_EMAIL
POSTGRES_PASSWORD=$(openssl rand -hex 24)
HEFESTO_SECRET=$(openssl rand -hex 32)
EOF
  chmod 600 "$HEFESTO_DIR/.env"
else
  log "Keeping existing $HEFESTO_DIR/.env"
fi

# 4. Up
log "Starting Hefesto stack..."
docker compose --project-directory "$HEFESTO_DIR" up -d

log "Done. Dashboard: https://$(grep '^HEFESTO_HOST=' "$HEFESTO_DIR/.env" | cut -d= -f2)"
log "Edit $HEFESTO_DIR/.env (HEFESTO_HOST, ACME_EMAIL) and re-run to reconfigure."
